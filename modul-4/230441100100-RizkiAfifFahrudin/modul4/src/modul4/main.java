public class AngkaRandom {
    int angka = 100;
    private int angkaAcak;
    Random random = new Random();
    DefaultListModel<String> modelScore = new DefaultListModel<>();
   
    public AngkaRandom() {
        angkaAcak = random.nextInt(angka);
    }

    public int getAngkaAcak() {
        return angkaAcak;
    }
    
    public void angkaAcakBaru(){
        angkaAcak = random.nextInt(angka);
    }
    
    public DefaultListModel<String> getModelScore() {
        return modelScore;
    }
}

AngkaRandom angkaRandom = new AngkaRandom();
    
    private void btnCekActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCekActionPerformed
        
    int angkaAcak = angkaRandom.getAngkaAcak();
    DefaultListModel<String> modelScore = angkaRandom.getModelScore();
    System.out.println(angkaAcak);
    

    if(angkaAcak == Integer.parseInt(txtTebakan.getText())) {
        txtCekAngka.setText("Aku adalah angka " + angkaAcak);
        int score = kesempatan*10;
        txtScore.setText(String.valueOf(score));
        
        String nama = JOptionPane.showInputDialog(null,"Masukan Nama Anda","Hebat",JOptionPane.INFORMATION_MESSAGE);
        
        modelScore.addElement(nama + " : Score "+score);
        jlScore.setModel(modelScore);
        
        txtTebakan.setText("");
        txtCekAngka.setText("");
        txtScore.setText("");
        kesempatan = 10;
        angkaRandom.angkaAcakBaru();
        lKesempatan.setText(String.valueOf(kesempatan));
        
        
    } else if(angkaAcak < Integer.parseInt(txtTebakan.getText())) {
        txtCekAngka.setText("Aku kurang dari " + txtTebakan.getText());
        kesempatan--;
        lKesempatan.setText(String.valueOf(kesempatan));
        
    } else if(angkaAcak > Integer.parseInt(txtTebakan.getText())) {
        txtCekAngka.setText("Aku lebih dari " + txtTebakan.getText());
        kesempatan--;
        lKesempatan.setText(String.valueOf(kesempatan));
        
    }

    if(kesempatan == 0) {
        JOptionPane.showMessageDialog(null, "WADUH SUDAH GAME OVER!!!", "WADUH", JOptionPane.INFORMATION_MESSAGE);
        angkaRandom.angkaAcakBaru();
        txtTebakan.setText("");
        txtCekAngka.setText("");
        txtScore.setText("");
        kesempatan = 10;
        angkaRandom.angkaAcakBaru();
        lKesempatan.setText(String.valueOf(kesempatan));
    }   
    }//GEN-LAST:event_btnCekActionPerformed

